import torch

# -----------------------------------------------------------------------------
# 1.1 Создание тензоров
# -----------------------------------------------------------------------------

def create_tensors():
    """Создаёт тензоры и возвращает их."""
    print("1.1 Создание тензоров")
    
    # Тензор размером 3x4, заполненный случайными числами от 0 до 1
    tensor_rand = torch.rand(3, 4)
    print(f"1) Случайный 3x4:\n{tensor_rand}\n")

    # Тензор размером 2x3x4, заполненный нулями
    tensor_zeros = torch.zeros(2, 3, 4)
    print(f"2) Нулевой 2x3x4:\n{tensor_zeros}\n")

    # Тензор размером 5x5, заполненный единицами
    tensor_ones = torch.ones(5, 5)
    print(f"3) Единичный 5x5:\n{tensor_ones}\n")

    # Тензор размером 4x4 с числами от 0 до 15 (reshape)
    tensor_arange = torch.arange(16).reshape(4, 4)
    print(f"4) 4x4 от 0 до 15:\n{tensor_arange}\n")

    return tensor_rand, tensor_zeros, tensor_ones, tensor_arange

# -----------------------------------------------------------------------------
# 1.2 Операции с тензорами
# -----------------------------------------------------------------------------

def tensor_operations():
    """Демонстрирует операции с тензорами A (3x4) и B (4x3)."""
    print("1.2 Операции с тензорами")

    # Тензоры A и B со случайными значениями
    A = torch.rand(3, 4)
    B = torch.rand(4, 3)
    print(f"A (3x4):\n{A}\n")
    print(f"B (4x3):\n{B}\n")

    # Транспонирование тензора A
    A_T = A.T
    print(f"Транспонирование A:\n{A_T}\n")

    # Матричное умножение A и B
    matmul_AB = torch.matmul(A, B)
    print(f"Матричное умножение A и B:\n{matmul_AB}\n")

    # Поэлементное умножение A и транспонированного B
    elemwise = A * B.T
    print(f"Поэлементное A * B.T:\n{elemwise}\n")

    # Сумма всех элементов A
    sum_A = torch.sum(A)
    print(f"Сумма всех элементов A: {sum_A.item():.4f}\n")

    return A, B

