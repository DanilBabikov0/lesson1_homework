def reshape_example():
    """Преобразование тензора из 24 элементов в различные формы."""
    print("1.4 Работа с формами")

    # Создаём тензор из 24 элементов
    t = torch.arange(24)
    print(f"Исходный тензор (24 элемента):\n{t}\n")

    shapes = [(2, 12), (3, 8), (4, 6), (2, 3, 4), (2, 2, 2, 3)]
    for shape in shapes:
        reshaped = t.reshape(shape)
        print(f"Reshape в {shape}:\n{reshaped}\n")

create_tensors()
tensor_operations()
indexing_slicing()
reshape_example()