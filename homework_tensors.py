# 1.3 Индексация и срезы
# -----------------------------------------------------------------------------

def indexing_slicing():
    """Работа с индексацией и срезами для тензора 5x5x5."""
    print("1.3 Индексация и срезы")

    # Создаём тензор 5x5x5
    tensor = torch.arange(0, 125).reshape(5, 5, 5)
    print(f"Исходный тензор 5x5x5:\n{tensor}\n")

    # Первая строка
    first_row = tensor[0, :, :]
    print(f"Первая строка (tensor[0, :, :]):\n{first_row}\n")

    # Последний столбец
    last_col = tensor[:, :, -1]
    print(f"Последний столбец (tensor[:, :, -1]):\n{last_col}\n")

    # Подматрица 2x2 из центра тензора
    submatrix = tensor[2, 1:3, 1:3]
    print(f"Подматрица 2x2 из центра (tensor[2, 1:3, 1:3]):\n{submatrix}\n")

    # Все элементы с чётными индексами
    even_indices = tensor[::2, ::2, ::2]
    print(f"Элементы с чётными индексами (tensor[::2, ::2, ::2]):\n{even_indices}\n")

    return tensor

